/**
 *      OBJECT
 *          É um tipo de dado estrutural
 * 
 *          Object
 *              - Propriedades / atributos
 *              - funcionalidades / métodos
 * 
 *          {propriedade: "valor"}
 */
console.log({
    // propriedade, ou seja, o atibutos
    name: "ibag",
    idade: 37,

    // funcionalidade, ou seja, o método
    andar: function() {
        console.log("Walking")
    }
})